package loopDemo;

public class loopDemoFor {

	public static void main(String[] args) {
		
		//1'den 10'a kadar (10 dahil) tam sayılar
		for(int i=1;i<=10;i++) {
				System.out.println(i);
		}
				System.out.println("Döngü bitti.");
		
				
		// 1'den 10'a kadar olan tek tam sayılar
		for(int i=1;i<10;i+=2) {
				System.out.println(i);
		}
				System.out.println("Döngü bitti.");
				
				
		// 1'den 10'a kadar çift tam sayılar
		for(int i=2;i<10;i+=2) {
				System.out.println(i);
		}
				System.out.println("Döngü bitti.");

	}

}
